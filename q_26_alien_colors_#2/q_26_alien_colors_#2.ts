// The if block is run
let alien_color = 'green';

if (alien_color === 'green') {
  console.log('You just earned 5 points for shooting the alien!');
} else {
  console.log('You just earned 10 points!');
}

console.log('******************************************************');

// The else block is run
alien_color = 'red';

if (alien_color === 'green') {
  console.log('You just earned 5 points for shooting the alien!');
} else {
  console.log('You just earned 10 points!');
}